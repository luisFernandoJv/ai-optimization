N_LAMPADAS = len(ILUMINA)
N_CASAS    = 400


def cobertura_valida(lampadas: list[bool]) -> bool:
    """Retorna True se todas as casas estão iluminadas."""
    iluminada = [False] * N_CASAS
    for i, ligada in enumerate(lampadas):
        if ligada:
            for casa in ILUMINA[i]:
                iluminada[casa] = True
    return all(iluminada)


class Estado:
    def __init__(self, lampadas: list[bool]):
        self.lampadas = lampadas
        self.f = sum(lampadas)  # número de lâmpadas ligadas (minimizar)

    def __repr__(self):
        return f"Estado(lâmpadas ligadas={self.f})"


def estado_inicial() -> Estado:
    return Estado([True] * N_LAMPADAS)


def gerar_vizinho(estado: Estado) -> Estado:
    """Tenta desligar uma lâmpada aleatória, mantendo cobertura total."""
    lampadas = estado.lampadas.copy()
    i = random.randint(0, N_LAMPADAS - 1)
    lampadas[i] = not lampadas[i]
    if cobertura_valida(lampadas):
        return Estado(lampadas)
    return estado  # mantém atual se violar cobertura


def melhor_vizinho(estado: Estado, n_tentativas: int = 5) -> Estado:
    vizinhos = [gerar_vizinho(estado) for _ in range(n_tentativas)]
    return min(vizinhos, key=lambda e: e.f)


def otimizar_iluminacao() -> Estado:
    atual = estado_inicial()
    while True:
        vizinho = melhor_vizinho(atual)
        if vizinho.f >= atual.f:
            break
        atual = vizinho
        print(f"  Melhorou: {atual}")
    return atual


